<h3>Quick guide to the Interactive Team work flow priority scale.</h3>

<br>

<ul class="alt">
	<li>1). Illegal.</li>
	<li>2). Page not displaying where surfaced highly.</li>
	<li>3). Page displaying with errors where surfaced highly. Page 1 same day or 1 day from go-live.</li>
	<li>4). Misleading content or assets. Obvious pages errors. Page 2 days from go-live.</li>
	<li>5). Campaign pages. 3 days from go-live.</li>
	<li>6). Standard content pages under normal circumstances.</li>
	<li>7). Ad-hoc build requests.</li>
	<li>8). Ad-hoc amends requests where not illegal or broken.</li>
	<li>9). Subjective amends, i.e. preferential amends, rephrasing copy or restructuring basic layout.</li>
	<li>10). On-going business proccesses, internal or none customer facing projects.</li>
</ul>